package clases;

public class Ubicacion {
	private int posx;
	private int posy;

	public Ubicacion(int posx, int posy) {
		this.posx = posx;
		this.posy = posy;
	}

	public int getPosx() {
		return posx;
	}

	public void setPosx(int posx) {
		this.posx = posx;
	}

	public int getPosy() {
		return posy;
	}

	public void setPosy(int posy) {
		this.posy = posy;
	}


}
